    private var innerStories: [Story] = []
    
    private init() {
        innerStories = [
            Story(spotName: "서동시장", questName: "금가락지 소동", scenarioImage: "저잣거리", characterImage: "girl", arrScenario: dialogues_Market, bgm: "market"),
